C
      IF (.NOT.DONE) KDISPI= -1
      IF (.NOT.DONE) KDISPO= -1
C
C          THE FOLLOWING ARE GRIDFILE-HANDLER COMMON BLOCKS:
C          ------------------------------------------------
C
      IBLOKL= 512
      FIRST= .TRUE.
      IFILCO= 1
      IDGRID= 555819264
