C
      KOLIN0= 0
      ITHIK0= 1
      MARKA0= 0
      MISSA0= 0
      MARKB0= 0
      MISSB0= 0
C
C          MAPPING VALUES:
C
      MRKMAP= 0
      MRKWIN= 0
      X1MAPV= 0.0
      X2MAPV= 1.0
      Y1MAPV= 0.0
      Y2MAPV= 1.0
      X1MAPD= 0.1
      X2MAPD= 0.95
