C          CURVE-DRAWING METHOD NUMBER:
C
      MCURV0= 1
C
C          CHARACTER VISIBILITY MODES:
C
      KWIND0= 0
      KMASK0= 0
      IDRBND= 1
C
C          CHANNEL INDICATOR/DEVICE RESOLUTION FACTOR:
C
      KCHAN0(1)= 0
      KCHAN0(2)= 0
      KCHAN0(3)= 0
      KCHAN0(4)= 0
      KCHAN0(5)= 0
      IRESL0= 1
C
C          CROSS-HATCHING VALUES:
C
      IFLTY0= 0
      IFLRP0= 0
      IHATN0= 65
C
      DO 100 ISET= 1,255
        HTANG0(ISET,1)= 0.785398
        HTANG0(ISET,2)= -0.785398
        IHCOL0(ISET,1)= 0
        IHCOL0(ISET,2)= 0
        NHFLG0(ISET)= 0
        IHMKA0(ISET,1)= 0
