      DO 200 ISET= 1,24
        HTANG0(ISET,1)= HTANGA(ISET,1)
        HTANG0(ISET,2)= HTANGA(ISET,2)
        IPITH0(ISET,1)= IPITHA(ISET,1)
        IPITH0(ISET,2)= IPITHA(ISET,2)
        IHAT0(ISET)= IHATA(ISET)
