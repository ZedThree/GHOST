        IHMSA0(ISET,1)= 0
        IHMSA0(ISET,2)= 0
        IHMKB0(ISET,1)= 0
        IHMKB0(ISET,2)= 0
        IHMSB0(ISET,1)= 0
        IHMSB0(ISET,2)= 0
        ISHFT0(ISET,1)= 0
        ISHFT0(ISET,2)= 0
        IPHAS0(ISET,1)= 0
        IPHAS0(ISET,2)= 0
        IPITH0(ISET,1)= 10
        IPITH0(ISET,2)= 10
