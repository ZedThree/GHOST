      SCALX0= 1.0
      SCALY0= 1.0
      ROTAT0= 0.0
      RPICX0= 0.0
      RPICY0= 0.0
      RDEVX0= 0.0
      RDEVY0= 0.0
      VRPICX= X1MAP0
      VRPICY= Y1MAP0
      VRDEVX= X1MAP0
      VRDEVY= Y1MAP0
      SHIFT0= .FALSE.
      MTRAN0= 1
      KLIPM0= 0
C
C          WINDOW VALUES:
C
      X1WND0= 0.0
      X2WND0= 1.0
      Y1WND0= 0.0
      Y2WND0= 1.0
