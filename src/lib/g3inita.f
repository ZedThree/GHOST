C
C          FLAGS FOR INCBAR, INCHIS, MULBAR AND MULHIS.
C
      DO 600 ISET= 1,100
        INFLGS(ISET)= 1
  600 CONTINUE
C
C          ANNOTATION MODES:
C
      NOTATA= 10
      NOTATC= 62
C
C          PIECHART FIRST SECTOR ANGLE:
C
